import math


#REDONDEAR HACIA ABAJO
resultado = math.floor(89.66177)
print(resultado)

#REDONDEAR HACIA ARRIBA
resultado = math.ceil(89.66177)
print(resultado)

resultado = math.pi
print(resultado)

#EL SEGUNDO NUMERO ELEVADO A CUAL NOS DARIA EL PRIMER NUMERO
resultado = math.log(25, 5)
print(resultado)

#tangente
resultado = math.tan(2556)
print(resultado)

#COSENO
resultado = math.cos(2556)
print(resultado)